<template>
  <div class="app-container">
    <!-- 统计数据生成表单 -->
    <el-form :inline="true" class="demo-form-inline">
      <!-- 生成数据日期 -->
      <el-form-item label="日期">
        <el-date-picker
          v-model="day"
          type="date"
          placeholder="选择要统计的日期"
          value-format="yyyy-MM-dd" />
      </el-form-item>
      <!-- 点生成按钮 -->
      <el-button
        :disabled="btnDisabled"
        type="primary"
        @click="create()">生成</el-button>
    </el-form>
  </div>
</template>

<script>
import statisticsApi from '@/api/statistics'
export default {
  data() {
    return {
      day: '',
      btnDisabled: false
    }
  },
  methods: {
    // 生成统计数据
    createData() {
      statisticsApi.createStatisticsData(this.day)
        .then(response => {
          // 提示信息
          this.$message({
            type: 'success',
            message: '生成数据成功！'
          })
          // 跳转图表显示页面
          this.$router.push({path: '/statistics/show'})
        })
    }
  }
}
</script>